package com.kuma.boot.datasource.config;

import org.springframework.boot.context.properties.ConfigurationProperties;

/**
 * 数据源扩展配置，可配合 spring.datasource 使用
 *
 * @author kuma
 */
@ConfigurationProperties(prefix = "kuma.datasource")
public class DataSourceProperties {

    private String url;
    private String username;
    private String password;
    private String driverClassName;
    private String poolName = "HikariPool";
    private int maximumPoolSize = DataSourceConstants.DEFAULT_POOL_SIZE;
    private int minimumIdle = DataSourceConstants.DEFAULT_MIN_IDLE;
    private long connectionTimeout = DataSourceConstants.DEFAULT_CONNECTION_TIMEOUT;
    private long idleTimeout = DataSourceConstants.DEFAULT_IDLE_TIMEOUT;
    private long maxLifetime = DataSourceConstants.DEFAULT_MAX_LIFETIME;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDriverClassName() {
        return driverClassName;
    }

    public void setDriverClassName(String driverClassName) {
        this.driverClassName = driverClassName;
    }

    public String getPoolName() {
        return poolName;
    }

    public void setPoolName(String poolName) {
        this.poolName = poolName;
    }

    public int getMaximumPoolSize() {
        return maximumPoolSize;
    }

    public void setMaximumPoolSize(int maximumPoolSize) {
        this.maximumPoolSize = maximumPoolSize;
    }

    public int getMinimumIdle() {
        return minimumIdle;
    }

    public void setMinimumIdle(int minimumIdle) {
        this.minimumIdle = minimumIdle;
    }

    public long getConnectionTimeout() {
        return connectionTimeout;
    }

    public void setConnectionTimeout(long connectionTimeout) {
        this.connectionTimeout = connectionTimeout;
    }

    public long getIdleTimeout() {
        return idleTimeout;
    }

    public void setIdleTimeout(long idleTimeout) {
        this.idleTimeout = idleTimeout;
    }

    public long getMaxLifetime() {
        return maxLifetime;
    }

    public void setMaxLifetime(long maxLifetime) {
        this.maxLifetime = maxLifetime;
    }
}
